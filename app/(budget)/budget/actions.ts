const PARTNER_CITIES = ["Bangalore", "Chennai", "Others"];

/** Move a partner (and all its budgets) to another city. Keeps the
 *  partner↔budget city invariant intact by migrating both together. Refuses if a
 *  partner of the same name already lives in the target city (the [city,name]
 *  unique). Budget-admin/super-admin only. */
export async function reassignGrantPartnerCity(partnerId: string, newCity: string): Promise<{ movedBudgets: number }> {
  const session = await auth();
  if (!isBudgetAdminOrSuperAdmin(session)) throw new Error("Forbidden");
  const city = newCity.trim();
  if (!PARTNER_CITIES.includes(city)) throw new Error("Unknown city");

  const gp = await prisma.grantPartner.findUnique({ where: { id: partnerId }, select: { name: true, city: true } });
  if (!gp) throw new Error("Partner not found");
  if (gp.city === city) return { movedBudgets: 0 };

  const clash = await prisma.grantPartner.findUnique({
    where: { city_name: { city, name: gp.name } },
    select: { id: true },
  });
  if (clash) throw new Error(`A partner named "${gp.name}" already exists in ${city}. Rename or merge it there first.`);

  const movedBudgets = await prisma.$transaction(async (tx) => {
    await tx.grantPartner.update({ where: { id: partnerId }, data: { city } });
    const res = await tx.budget.updateMany({ where: { grantPartnerId: partnerId }, data: { city } });
    return res.count;
  });

  revalidatePath("/budget/admin/partners");
  revalidatePath("/budget/dashboard");
  revalidatePath("/budget");
  return { movedBudgets };
}

// Link an existing user account as this grantee's login (role "partner"). The
// account must be created first in Settings → Users. Only promotes a plain
// member/viewer/partner — never touches an admin/super-admin/budget-admin.
export async function linkGrantPartnerLogin(partnerId: string, email: string): Promise<{ email: string }> {
  const session = await auth();
  if (!isBudgetAdminOrSuperAdmin(session)) throw new Error("Forbidden");
  const normalized = email.trim().toLowerCase();
  if (!normalized) throw new Error("Email required");
  const user = await prisma.user.findFirst({
    where: { email: { equals: normalized, mode: "insensitive" } },
    select: { id: true, email: true, role: true },
  });
  if (!user) throw new Error("No user with that email. Create the account in Settings → Users first, then link it here.");

  const PROMOTABLE = ["member", "viewer", "partner"];
  await prisma.$transaction(async (tx) => {
    // Enforce one grantee per login: drop any other partner's link to this user.
    await tx.grantPartner.updateMany({ where: { userId: user.id, NOT: { id: partnerId } }, data: { userId: null } });
    await tx.grantPartner.update({ where: { id: partnerId }, data: { userId: user.id } });
    if (PROMOTABLE.includes(user.role)) {
      await tx.user.update({ where: { id: user.id }, data: { role: "partner" } });
    }
  });
  revalidatePath("/budget/admin/partners");
  return { email: user.email };
}

export async function unlinkGrantPartnerLogin(partnerId: string) {
  const session = await auth();
  if (!isBudgetAdminOrSuperAdmin(session)) throw new Error("Forbidden");
  await prisma.grantPartner.update({ where: { id: partnerId }, data: { userId: null } });
  revalidatePath("/budget/admin/partners");
}

/** One-way import: copy every active Settings → Partners org (Org kind="partner")
 *  into GrantPartner for the given city. Idempotent on [city, name]; existing
 *  rows are left untouched (and reactivated if they were deactivated). Returns
 *  how many were newly created. */
export async function importGrantPartnersFromOrgs(city: string): Promise<{ added: number; total: number }> {
  const session = await auth();
  if (!session?.user?.id) throw new Error("Not authenticated");
  const trimmedCity = city.trim();
  if (!trimmedCity) throw new Error("City required");

  const orgs = await prisma.org.findMany({
    where: { kind: "partner", archivedAt: null },
    select: { name: true },
    orderBy: { name: "asc" },
  });

  let added = 0;
  for (const o of orgs) {
    const name = o.name.trim();
    if (!name) continue;
    const existing = await prisma.grantPartner.findUnique({
      where: { city_name: { city: trimmedCity, name } },
      select: { id: true },
    });
    if (existing) continue;
    await prisma.grantPartner.create({ data: { city: trimmedCity, name } });
    added++;
  }

  revalidatePath("/budget/admin/partners");
  revalidatePath("/budget/dashboard");
  return { added, total: orgs.length };
}

/** (Re)assign a budget to a grantee org, or unassign (null). Owner-only; the
 *  partner must belong to the same city as the budget. */
export async function updateBudgetGrantPartner(budgetId: string, grantPartnerId: string | null) {
  const session = await auth();
  if (!session?.user?.id) throw new Error("Not authenticated");

  const budget = await prisma.budget.findUnique({ where: { id: budgetId }, select: { partnerId: true, city: true } });
  if (!budget || budget.partnerId !== session.user.id) throw new Error("Not found");

  if (grantPartnerId) {
    const gp = await prisma.grantPartner.findUnique({ where: { id: grantPartnerId }, select: { city: true } });
    if (!gp || gp.city !== budget.city) throw new Error("Partner is not in this budget's city");
  }

  await prisma.budget.update({ where: { id: budgetId }, data: { grantPartnerId } });
  revalidatePath(`/budget/${budgetId}`);
  revalidatePath("/budget/dashboard");
}

/**
 * Freeze each generated line's "working": snapshot the standard registry
 * component breakup (+ derivation) onto the line so the budget carries a
 * faithful point-in-time record (immune to later registry changes). Left
 * workingCustomised=false — a create-time cost override changes the COST, not
 * the working, and the reconcile ⚠ already flags where the frozen standard
 * breakup no longer sums to the line's cost. Also logs a "generated at ₹X"
 * origin entry per costed line.
 */
async function snapshotLineWorking(
  budgetId: string,
  sourceCity: string,
  templates: { templateKey: string; costKey: string | null }[],
  changedById: string,
) {
  const [comps, regItems, lines] = await Promise.all([
    prisma.costRegistryComponent.findMany({ where: { city: sourceCity }, orderBy: { position: "asc" }, select: { parentItemKey: true, label: true, spec: true, qty: true, unitCost: true } }),
    prisma.costRegistry.findMany({ where: { city: sourceCity }, select: { itemKey: true, derivation: true } }),
    prisma.budgetLine.findMany({ where: { budgetId }, select: { id: true, templateKey: true, y1UnitCost: true } }),
  ]);
  const compByKey = new Map<string, typeof comps>();
  for (const c of comps) { const a = compByKey.get(c.parentItemKey) ?? []; a.push(c); compByKey.set(c.parentItemKey, a); }
  const derivByKey = new Map(regItems.map(r => [r.itemKey, r.derivation]));
  const costKeyByTemplate = new Map(templates.map(t => [t.templateKey, t.costKey]));

  const componentRows: { budgetLineId: string; position: number; label: string; spec: string | null; qty: number; unitCost: number }[] = [];
  const historyRows: { budgetLineId: string; oldCost: null; newCost: number; source: string; changedById: string }[] = [];
  const derivUpdates: { id: string; derivation: string }[] = [];

  for (const l of lines) {
    if (l.y1UnitCost > 0) historyRows.push({ budgetLineId: l.id, oldCost: null, newCost: l.y1UnitCost, source: "generated", changedById });
    const costKey = l.templateKey ? costKeyByTemplate.get(l.templateKey) ?? null : null;
    if (!costKey) continue;
    const cs = compByKey.get(costKey);
    if (!cs || cs.length === 0) continue;
    cs.forEach((c, i) => componentRows.push({ budgetLineId: l.id, position: i, label: c.label, spec: c.spec, qty: c.qty, unitCost: c.unitCost }));
    const d = derivByKey.get(costKey);
    if (d) derivUpdates.push({ id: l.id, derivation: d });
  }

  if (componentRows.length) await prisma.budgetLineComponent.createMany({ data: componentRows });
  if (historyRows.length) await prisma.budgetLineCostHistory.createMany({ data: historyRows });
  for (const u of derivUpdates) await prisma.budgetLine.update({ where: { id: u.id }, data: { derivation: u.derivation } });
}

export async function createBudget(payload: CreateBudgetPayload) {
  const session = await auth();
  if (!session?.user?.id) throw new Error("Not authenticated");

  const horizonMonths = Math.min(60, Math.max(1, Math.round(payload.horizonMonths)));
  const partialPosition: "start" | "end" = payload.partialPosition === "start" ? "start" : "end";
  // Keep `years` in sync for legacy read paths (list pages, report-actions
  // grantYear lookups, etc.). years = number of year-bands the horizon covers.
  const years = activeYearBands(horizonMonths);

  // "Others" is a placeholder city for test / cross-city mock budgets — it has
  // no CostRegistry or LineTemplate rows of its own. Fall back to Bangalore's
  // (same fallback the new-budget form applies for domain visibility) so the
  // generator produces non-zero lines. Budget.city itself still stores "Others"
  // so the budget stays under the Others tab.
  const sourceCity = payload.city === "Others" ? "Bangalore" : payload.city;
  const [registryRows, templates] = await Promise.all([
    prisma.costRegistry.findMany({ where: { city: sourceCity } }),
    prisma.lineTemplate.findMany({ where: { city: sourceCity }, orderBy: { position: "asc" } }),
  ]);
  // Snapshot the full registry at create time. Stored on Budget.costSnapshot
  // so future regenerate / scale-up actions reproduce the partner's basis
  // even if the registry has since changed.
  const costSnapshot: Record<string, number> = Object.fromEntries(registryRows.map(r => [r.itemKey, r.unitCost]));
  // Drop overrides for keys not in the registry so we never persist orphan keys.
  const rawOverrides = payload.costOverrides ?? {};
  const costOverrides: Record<string, number> = {};
  for (const [k, v] of Object.entries(rawOverrides)) {
    if (typeof v === "number" && Number.isFinite(v) && k in costSnapshot) {
      costOverrides[k] = v;
    }
  }
  // Generator reads snapshot ∪ overrides — overrides win where present.
  const mergedRegistry: Record<string, number> = { ...costSnapshot, ...costOverrides };

  const genOpts = { horizonMonths, applyInflation: payload.applyInflation, inflationRates: DEFAULT_INFLATION_RATES, partialPosition };

  // ── Multi-partner path ──────────────────────────────────────────────────────
  const partners = (payload.deliveryPartners ?? []).filter(p => p.name?.trim());
  if (partners.length >= 2) {
    const summed = sumInputs(partners.map(p => p.programmeInputs));
    const directTemplates = templates.filter(t => t.domain !== null);
    const sharedTemplates = templates.filter(t => t.domain === null);

    // Each partner's domain lines come from its own inputs; shared/cross-cutting
    // lines are generated once from the summed totals into the Master.
    const perPartnerLines = partners.map(p =>
      generateBudgetLines(payload.domains, p.programmeInputs, genOpts, mergedRegistry, directTemplates));
    const sharedLines = payload.includeCrossCutting
      ? generateBudgetLines(payload.domains, summed, genOpts, mergedRegistry, sharedTemplates)
      : [];

    const budget = await prisma.budget.create({
      data: {
        name: payload.name, city: payload.city, partnerId: session.user.id,
        grantPartnerId: payload.grantPartnerId ?? null,
        domains: payload.domains, years, horizonMonths, partialPosition,
        applyInflation: payload.applyInflation, costSnapshot, costOverrides,
        isMultiPartner: true,
        inputs: { create: buildInputsCreate(summed) },
        deliveryPartners: {
          create: partners.map((p, i) => ({
            name: p.name.trim(), sortOrder: i,
            sharedPct: Number.isFinite(p.sharedPct) ? p.sharedPct : 0,
            inputs: p.programmeInputs,
          })),
        },
      },
      include: { deliveryPartners: true },
    });

    const idByOrder = new Map(budget.deliveryPartners.map(dp => [dp.sortOrder, dp.id]));
    const lineRows = [
      ...perPartnerLines.flatMap((ls, i) => ls.map(l => ({ ...lineCreateData(l), budgetId: budget.id, deliveryPartnerId: idByOrder.get(i) ?? null }))),
      ...sharedLines.map(l => ({ ...lineCreateData(l), budgetId: budget.id, deliveryPartnerId: null })),
    ];
    if (lineRows.length) await prisma.budgetLine.createMany({ data: lineRows });

    await snapshotLineWorking(budget.id, sourceCity, templates, session.user.id);
    redirect(`/budget/${budget.id}`);
  }

  const eligibleTemplates = payload.includeCrossCutting
    ? templates
    : templates.filter(t => t.domain !== null);
  const lines = generateBudgetLines(
    payload.domains,
    payload.programmeInputs,
    genOpts,
    mergedRegistry,
    eligibleTemplates,
  );

  const pi = payload.programmeInputs;
  const budget = await prisma.budget.create({
    data: {
      name: payload.name,
      city: payload.city,
      partnerId: session.user.id,
      grantPartnerId: payload.grantPartnerId ?? null,
      domains: payload.domains,
      years,
      horizonMonths,
      partialPosition,
      applyInflation: payload.applyInflation,
      costSnapshot,
      costOverrides,
      // Inflation rates fall back to the Prisma schema defaults (10/5/0) if omitted.
      inputs: {
        create: {
          // Keep typed fields populated for backward compat with existing queries
          nSettlements:              Math.round(pi.nSettlements ?? 0),
          nClusters:                 Math.round(pi.nClusters ?? 0),
          nCLCs:                     Math.round(pi.nCLCs ?? 0),
          clcRentPerMonth:           Math.round(pi.clcRentPerMonth ?? 0),
          nYRCs:                     Math.round(pi.nYRCs ?? 0),
          yrcRentPerMonth:           Math.round(pi.yrcRentPerMonth ?? 0),
          nElderlyCentres:           Math.round(pi.nElderlyCentres ?? 0),
          nElderly:                  Math.round(pi.nElderly ?? 0),
          elderlyCentreRentPerMonth: Math.round(pi.elderlyCentreRentPerMonth ?? 0),
          cosPerCluster:             Math.round(pi.cosPerCluster ?? 0),
          rcRentPerMonth:            Math.round(pi.rcRentPerMonth ?? 0),
          nCreches:                  Math.round(pi.nCreches ?? 0),
          crecheRentPerMonth:        Math.round(pi.crecheRentPerMonth ?? 0),
          extraInputs:               pi,  // full flat map — source of truth going forward
        },
      },
      lines: {
        create: lines.map(l => ({
          domain: l.domain ?? undefined,
          section: l.section,
          position: l.position,
          description: l.description,
          costCategory: l.costCategory,
          unitType: l.unitType,
          isAutoGenerated: l.isAutoGenerated ?? true,
          salaryHint: l.salaryHint,
          notes: l.notes,
          templateKey: l.templateKey,
          cadence: l.cadence,
          plannedMonths: l.plannedMonths,
          y1Units: l.y1Units,
          y1UnitCost: l.y1UnitCost,
          y1AllocPct: l.y1AllocPct,
          y1Total: l.y1Total,
          y2Units: l.y2Units,
          y2UnitCost: l.y2UnitCost,
          y2AllocPct: l.y2AllocPct,
          y2Total: l.y2Total,
          y3Units: l.y3Units,
          y3UnitCost: l.y3UnitCost,
          y3AllocPct: l.y3AllocPct,
          y3Total: l.y3Total,
          y4Units: l.y4Units,
          y4UnitCost: l.y4UnitCost,
          y4AllocPct: l.y4AllocPct,
          y4Total: l.y4Total,
          y5Units: l.y5Units,
          y5UnitCost: l.y5UnitCost,
          y5AllocPct: l.y5AllocPct,
          y5Total: l.y5Total,
        })),
      },
    },
  });

  await snapshotLineWorking(budget.id, sourceCity, templates, session.user.id);
  redirect(`/budget/${budget.id}`);
}

/**
 * Create a budget from a parsed (imported) Excel template. Mirrors createBudget's
 * write but uses the parsed lines/inputs verbatim instead of the generator, and
 * stamps importedAt so a future "regenerate from inputs" knows the lines carry
 * hand edits. Returns the new id (no redirect) so the client can navigate.
 */
export async function createBudgetFromImport(
  parsed: ParsedBudget,
  opts?: { city?: string; grantPartnerId?: string | null },
): Promise<{ id: string }> {
  const session = await auth();
  if (!session?.user?.id) throw new Error("Not authenticated");

  const horizonMonths = Math.min(60, Math.max(1, Math.round(parsed.horizonMonths || 12)));
  const years = activeYearBands(horizonMonths);
  const pi = parsed.inputs ?? {};
  const num = (k: string) => Math.round(Number(pi[k]) || 0);

  const budget = await prisma.budget.create({
    data: {
      name: parsed.name,
      city: opts?.city?.trim() || parsed.city,
      grantPartnerId: opts?.grantPartnerId ?? null,
      partnerId: session.user.id,
      domains: parsed.domains,
      years,
      horizonMonths,
      applyInflation: parsed.applyInflation,
      inflationSalaryPct: parsed.inflationPct.Salary,
      inflationOtherPct: parsed.inflationPct.Other,
      inflationNilPct: parsed.inflationPct.Nil,
      costSnapshot: parsed.costSnapshot,
      costOverrides: parsed.costOverrides,
      importedAt: new Date(),
      inputs: {
        create: {
          nSettlements: num("nSettlements"), nClusters: num("nClusters"),
          nCLCs: num("nCLCs"), clcRentPerMonth: num("clcRentPerMonth"),
          nYRCs: num("nYRCs"), yrcRentPerMonth: num("yrcRentPerMonth"),
          nElderlyCentres: num("nElderlyCentres"), nElderly: num("nElderly"),
          elderlyCentreRentPerMonth: num("elderlyCentreRentPerMonth"),
          cosPerCluster: num("cosPerCluster"), rcRentPerMonth: num("rcRentPerMonth"),
          nCreches: num("nCreches"), crecheRentPerMonth: num("crecheRentPerMonth"),
          extraInputs: pi,
        },
      },
      lines: {
        create: parsed.lines
          .slice()
          .sort((a, b) => a.position - b.position)
          .map((l, i) => ({
            domain: l.domain ?? undefined,
            section: l.section as BudgetSection,
            position: i,
            description: l.description,
            costCategory: l.costCategory as InflationType,
            unitType: l.unitType,
            isAutoGenerated: false,
            salaryHint: l.salaryHint,
            notes: l.notes,
            templateKey: l.templateKey,
            cadence: l.cadence as BudgetLineCadence,
            plannedMonths: l.plannedMonths,
            y1Units: l.y1Units, y1UnitCost: l.y1UnitCost, y1AllocPct: l.y1AllocPct, y1Total: l.y1Total,
            y2Units: l.y2Units, y2UnitCost: l.y2UnitCost, y2AllocPct: l.y2AllocPct, y2Total: l.y2Total,
            y3Units: l.y3Units, y3UnitCost: l.y3UnitCost, y3AllocPct: l.y3AllocPct, y3Total: l.y3Total,
            y4Units: l.y4Units, y4UnitCost: l.y4UnitCost, y4AllocPct: l.y4AllocPct, y4Total: l.y4Total,
            y5Units: l.y5Units, y5UnitCost: l.y5UnitCost, y5AllocPct: l.y5AllocPct, y5Total: l.y5Total,
          })),
      },
    },
  });

  revalidatePath("/budget");
  return { id: budget.id };
}

/**
 * Normalises a (cadence, plannedMonths) pair from the client into a shape
 * the DB will accept: dedupe, sort, clamp to 1..12, enforce arity per cadence.
 * Throws on bad input — keep the server honest even if the UI guards.
 */
function normaliseCadence(
  cadence: BudgetLineCadence,
  plannedMonths: number[] | undefined,
): { cadence: BudgetLineCadence; plannedMonths: number[] } {
  if (cadence === "monthly") return { cadence, plannedMonths: [] };
  const cleaned = Array.from(new Set((plannedMonths ?? [])
    .map(m => Math.round(m))
    .filter(m => Number.isFinite(m) && m >= 1 && m <= 12)))
    .sort((a, b) => a - b);
  if (cadence === "one_time" && cleaned.length !== 1) {
    throw new Error("one_time cadence requires exactly one planned month (1..12)");
  }
  if (cadence === "seasonal" && cleaned.length < 2) {
    throw new Error("seasonal cadence requires at least two planned months");
  }
  return { cadence, plannedMonths: cleaned };
}

export async function updateLine(
  lineId: string,
  updates: {
    description?: string;
    // Inflation category (Salary/Other/Nil) — omit to leave unchanged. The
    // editor recomputes the out-year unit costs before sending.
    costCategory?: InflationType;
    // domain: pass null to mark the row as cross-cutting, string to attach it
    // to a specific domain card. Omit to leave unchanged.
    domain?: string | null;
    // Cadence: omit to leave unchanged. Pass cadence + plannedMonths together.
    cadence?: BudgetLineCadence;
    plannedMonths?: number[];
    y1Units?: number; y1UnitCost?: number; y1AllocPct?: number;
    y2Units?: number; y2UnitCost?: number; y2AllocPct?: number;
    y3Units?: number; y3UnitCost?: number; y3AllocPct?: number;
    y4Units?: number; y4UnitCost?: number; y4AllocPct?: number;
    y5Units?: number; y5UnitCost?: number; y5AllocPct?: number;
  }
) {
  const session = await auth();
  if (!session?.user?.id) throw new Error("Not authenticated");

  const line = await prisma.budgetLine.findUnique({ where: { id: lineId }, select: { budgetId: true, budget: { select: { partnerId: true } }, costCategory: true, y1UnitCost: true } });
  if (!line || line.budget.partnerId !== session.user.id) throw new Error("Not found");

  const total = (u?: number, c?: number, a?: number) => Math.round((u ?? 0) * (c ?? 0) * (a ?? 1));
  const y1Total = total(updates.y1Units, updates.y1UnitCost, updates.y1AllocPct);
  const y2Total = total(updates.y2Units, updates.y2UnitCost, updates.y2AllocPct);
  const y3Total = total(updates.y3Units, updates.y3UnitCost, updates.y3AllocPct);
  const y4Total = total(updates.y4Units, updates.y4UnitCost, updates.y4AllocPct);
  const y5Total = total(updates.y5Units, updates.y5UnitCost, updates.y5AllocPct);

  // Strip + re-validate the cadence pair so we never persist {one_time, []} etc.
  const { cadence: _c, plannedMonths: _pm, ...rest } = updates;
  const cadencePatch = updates.cadence !== undefined
    ? normaliseCadence(updates.cadence, updates.plannedMonths)
    : {};

  // A base (Year-1) unit-cost change marks the line as customised on this budget.
  const costChanged = updates.y1UnitCost !== undefined && Math.round(updates.y1UnitCost) !== Math.round(line.y1UnitCost);
  await prisma.budgetLine.update({
    where: { id: lineId },
    data: { ...rest, ...cadencePatch, y1Total, y2Total, y3Total, y4Total, y5Total, ...(costChanged ? { workingCustomised: true } : {}) },
  });
  if (costChanged) {
    await prisma.budgetLineCostHistory.create({
      data: { budgetLineId: lineId, oldCost: line.y1UnitCost, newCost: updates.y1UnitCost!, source: "line edit", changedById: session.user.id },
    });
  }
  revalidatePath(`/budget/${line.budgetId}`);
}

/** History for a budget line's base unit cost, newest first. */
export async function getBudgetLineHistory(lineId: string) {
  const session = await auth();
  if (!session?.user?.id) throw new Error("Not authenticated");
  const line = await prisma.budgetLine.findUnique({ where: { id: lineId }, select: { budget: { select: { partnerId: true } } } });
  if (!line || line.budget.partnerId !== session.user.id) throw new Error("Not found");
  const rows = await prisma.budgetLineCostHistory.findMany({ where: { budgetLineId: lineId }, orderBy: { changedAt: "desc" }, take: 50 });
  const byId = new Map(
    (await prisma.user.findMany({
      where: { id: { in: [...new Set(rows.map(r => r.changedById).filter((v): v is string => !!v))] } },
      select: { id: true, name: true, email: true },
    })).map(u => [u.id, u.name ?? u.email])
  );
  return rows.map(r => ({
    id: r.id, oldCost: r.oldCost, newCost: r.newCost, source: r.source,
    changedBy: r.changedById ? (byId.get(r.changedById) ?? null) : null,
    changedAt: r.changedAt.toISOString(),
  }));
}

/**
 * Author a budget line's working: replace its component breakup and set the
 * base (Year-1) unit cost to the rollup. Later-year unit costs are scaled by the
 * same ratio so the line keeps its inflation shape. Totals recompute; the change
 * is logged. This is the budget-level mirror of the registry working editor.
 */
export async function saveBudgetLineComponents(
  lineId: string,
  rows: { label: string; spec?: string | null; qty: number; unitCost: number }[],
  derivation: string | null,
) {
  const session = await auth();
  if (!session?.user?.id) throw new Error("Not authenticated");
  const line = await prisma.budgetLine.findUnique({
    where: { id: lineId },
    select: {
      budgetId: true, budget: { select: { partnerId: true } },
      y1Units: true, y1UnitCost: true, y1AllocPct: true,
      y2Units: true, y2UnitCost: true, y2AllocPct: true,
      y3Units: true, y3UnitCost: true, y3AllocPct: true,
      y4Units: true, y4UnitCost: true, y4AllocPct: true,
      y5Units: true, y5UnitCost: true, y5AllocPct: true,
    },
  });
  if (!line || line.budget.partnerId !== session.user.id) throw new Error("Not found");

  const clean = rows
    .map(r => ({ label: (r.label ?? "").trim(), spec: (r.spec ?? "")?.toString().trim() || null, qty: Number(r.qty) || 0, unitCost: Number(r.unitCost) || 0 }))
    .filter(r => r.label.length > 0);
  const rollup = Math.round(clean.reduce((s, r) => s + r.qty * r.unitCost, 0));
  const deriv = derivation?.trim() || null;

  // Scale later years by the same ratio the edit applies to Year 1, preserving
  // the existing inflation curve. If Year-1 was 0, fall back to a flat rollup.
  const ratio = line.y1UnitCost > 0 ? rollup / line.y1UnitCost : 1;
  const scaled = (base: number) => (line.y1UnitCost > 0 ? Math.round(base * ratio) : rollup);
  const y1c = clean.length > 0 ? rollup : line.y1UnitCost;
  const y2c = clean.length > 0 ? scaled(line.y2UnitCost) : line.y2UnitCost;
  const y3c = clean.length > 0 ? scaled(line.y3UnitCost) : line.y3UnitCost;
  const y4c = clean.length > 0 ? scaled(line.y4UnitCost) : line.y4UnitCost;
  const y5c = clean.length > 0 ? scaled(line.y5UnitCost) : line.y5UnitCost;
  const total = (u: number, c: number, a: number) => Math.round(u * c * a);

  await prisma.$transaction(async (tx) => {
    await tx.budgetLineComponent.deleteMany({ where: { budgetLineId: lineId } });
    if (clean.length > 0) {
      await tx.budgetLineComponent.createMany({
        data: clean.map((r, i) => ({ budgetLineId: lineId, position: i, label: r.label, spec: r.spec, qty: r.qty, unitCost: r.unitCost })),
      });
    }
    await tx.budgetLine.update({
      where: { id: lineId },
      data: {
        derivation: deriv,
        workingCustomised: true,
        y1UnitCost: y1c, y1Total: total(line.y1Units, y1c, line.y1AllocPct),
        y2UnitCost: y2c, y2Total: total(line.y2Units, y2c, line.y2AllocPct),
        y3UnitCost: y3c, y3Total: total(line.y3Units, y3c, line.y3AllocPct),
        y4UnitCost: y4c, y4Total: total(line.y4Units, y4c, line.y4AllocPct),
        y5UnitCost: y5c, y5Total: total(line.y5Units, y5c, line.y5AllocPct),
      },
    });
    if (clean.length > 0 && Math.round(line.y1UnitCost) !== rollup) {
      await tx.budgetLineCostHistory.create({
        data: { budgetLineId: lineId, oldCost: line.y1UnitCost, newCost: rollup, source: "working editor", changedById: session.user!.id },
      });
    }
  });

  revalidatePath(`/budget/${line.budgetId}`);
}

export async function addLine(
  budgetId: string,
  data: {
    domain?: string;
    section: BudgetSection;
    description: string;
    costCategory: InflationType;
    unitType: string;
    y1Units?: number;
    y1UnitCost?: number;
    cadence?: BudgetLineCadence;
    plannedMonths?: number[];
    /** Multi-partner: which delivery partner this line belongs to (null = shared/Master). */
    deliveryPartnerId?: string | null;
  }
) {
  const session = await auth();
  if (!session?.user?.id) throw new Error("Not authenticated");

  const budget = await prisma.budget.findUnique({
    where: { id: budgetId },
    select: {
      partnerId: true,
      horizonMonths: true,
      applyInflation: true,
      inflationSalaryPct: true,
      inflationOtherPct: true,
      inflationNilPct: true,
      lines: { select: { position: true }, orderBy: { position: "desc" }, take: 1 },
    },
  });
  if (!budget || budget.partnerId !== session.user.id) throw new Error("Not found");

  const ratePct =
    data.costCategory === "Salary" ? budget.inflationSalaryPct
    : data.costCategory === "Other" ? budget.inflationOtherPct
    : budget.inflationNilPct;
  const rate = budget.applyInflation ? ratePct / 100 : 0;

  const y1Units = data.y1Units ?? 0;
  const y1UnitCost = data.y1UnitCost ?? 0;

  const yearFactor = (k: number): number => {
    const full = Math.floor(budget.horizonMonths / 12);
    const tail = budget.horizonMonths - full * 12;
    if (k <= full) return 1;
    if (k === full + 1 && tail > 0) return tail / 12;
    return 0;
  };

  const bandData = (k: number) => {
    const factor = yearFactor(k);
    if (factor === 0) return { units: 0, cost: 0, total: 0 };
    const units = y1Units * factor;
    const cost  = Math.round(y1UnitCost * Math.pow(1 + rate, k - 1));
    return { units, cost, total: Math.round(units * cost) };
  };

  const b1 = { units: y1Units, cost: y1UnitCost, total: Math.round(y1Units * y1UnitCost * yearFactor(1)) };
  const b2 = bandData(2);
  const b3 = bandData(3);
  const b4 = bandData(4);
  const b5 = bandData(5);

  const nextPos = (budget.lines[0]?.position ?? 0) + 1;
  // Defaults: Capex section → one_time month 1, everything else → monthly.
  // Partner can override via the cadence picker in the add-line form.
  const fallbackCadence: BudgetLineCadence = data.section === "capex" ? "one_time" : "monthly";
  const cadencePatch = normaliseCadence(
    data.cadence ?? fallbackCadence,
    data.cadence ? data.plannedMonths : (data.section === "capex" ? [1] : []),
  );
  const line = await prisma.budgetLine.create({
    data: {
      budgetId,
      deliveryPartnerId: data.deliveryPartnerId ?? null,
      domain: data.domain,
      section: data.section,
      description: data.description,
      costCategory: data.costCategory,
      unitType: data.unitType,
      position: nextPos,
      isAutoGenerated: false,
      ...cadencePatch,
      y1Units: b1.units, y1UnitCost: b1.cost, y1AllocPct: 1, y1Total: b1.total,
      y2Units: b2.units, y2UnitCost: b2.cost, y2AllocPct: 1, y2Total: b2.total,
      y3Units: b3.units, y3UnitCost: b3.cost, y3AllocPct: 1, y3Total: b3.total,
      y4Units: b4.units, y4UnitCost: b4.cost, y4AllocPct: 1, y4Total: b4.total,
      y5Units: b5.units, y5UnitCost: b5.cost, y5AllocPct: 1, y5Total: b5.total,
    },
  });
  revalidatePath(`/budget/${budgetId}`);
  return line;
}

