export type CreateBudgetPayload = {
  name: string;
  city: string;
  /** Grantee org (GrantPartner.id) this budget belongs to. Null = unassigned. */
  grantPartnerId?: string | null;
  domains: string[];
  horizonMonths: number;
  /** Where the pro-rated stub sits: "start" or "end" (default). */
  partialPosition?: "start" | "end";
  applyInflation: boolean;
  programmeInputs: Record<string, number>;
  includeCrossCutting: boolean;
  /** Sparse delta keyed by CostRegistry.itemKey. {} when partner picked "Standard". */
  costOverrides?: Record<string, number>;
  /**
   * Multi-partner mode. When 2+ delivery partners are given, each gets its own
   * domain lines from its own programmeInputs (tagged deliveryPartnerId), the
   * budget's inputs become the summed totals, and cross-cutting lines are
   * generated once into the Master (deliveryPartnerId null). sharedPct is each
   * partner's explicit share of those shared costs (consumed at display/export).
   */
  deliveryPartners?: { name: string; sharedPct: number; programmeInputs: Record<string, number> }[];
};

/** Element-wise sum of several flat input maps (for the master totals). */
function sumInputs(maps: Record<string, number>[]): Record<string, number> {
  const out: Record<string, number> = {};
  for (const m of maps) for (const [k, v] of Object.entries(m)) out[k] = (out[k] ?? 0) + (Number(v) || 0);
  return out;
}

/** Nested-create payload for a BudgetInputs row from a flat input map. */
function buildInputsCreate(pi: Record<string, number>) {
  return {
    nSettlements:              Math.round(pi.nSettlements ?? 0),
    nClusters:                 Math.round(pi.nClusters ?? 0),
    nCLCs:                     Math.round(pi.nCLCs ?? 0),
    clcRentPerMonth:           Math.round(pi.clcRentPerMonth ?? 0),
    nYRCs:                     Math.round(pi.nYRCs ?? 0),
    yrcRentPerMonth:           Math.round(pi.yrcRentPerMonth ?? 0),
    nElderlyCentres:           Math.round(pi.nElderlyCentres ?? 0),
    nElderly:                  Math.round(pi.nElderly ?? 0),
    elderlyCentreRentPerMonth: Math.round(pi.elderlyCentreRentPerMonth ?? 0),
    cosPerCluster:             Math.round(pi.cosPerCluster ?? 0),
    rcRentPerMonth:            Math.round(pi.rcRentPerMonth ?? 0),
    nCreches:                  Math.round(pi.nCreches ?? 0),
    crecheRentPerMonth:        Math.round(pi.crecheRentPerMonth ?? 0),
    extraInputs:               pi,
  };
}

