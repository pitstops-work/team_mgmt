import type { BudgetDomain, BudgetSection, InflationType } from "@/app/generated/prisma/client";

export type LineInput = {
  domain: BudgetDomain | null;
  section: BudgetSection;
  description: string;
  costCategory: InflationType;
  unitType: string;
  y1Units: number;
  y1UnitCost: number;
  y1AllocPct?: number;
  salaryHint?: string;
  notes?: string;
  isAutoGenerated?: boolean;
};

export type GeneratedLine = LineInput & {
  position: number;
  y1Total: number;
  y2Units: number; y2UnitCost: number; y2AllocPct: number; y2Total: number;
  y3Units: number; y3UnitCost: number; y3AllocPct: number; y3Total: number;
};

export type BudgetGeneratorInputs = {
  nSettlements: number;
  nClusters: number;
  nCLCs: number;
  clcRentPerMonth: number;
  nYRCs: number;
  yrcRentPerMonth: number;
  nElderlyCentres: number;
  nElderly: number;
  elderlyCentreRentPerMonth: number;
  cosPerCluster: number;
  rcRentPerMonth: number;
  nCreches: number;
  crecheRentPerMonth: number;
};

const INFLATION: Record<InflationType, number> = { Salary: 0.10, Other: 0.05, Nil: 0.00 };

function computeYears(spec: LineInput, years: number): Omit<GeneratedLine, keyof LineInput | "position"> {
  const alloc = spec.y1AllocPct ?? 1;
  const rate = INFLATION[spec.costCategory];
  const y1Total = Math.round(spec.y1Units * spec.y1UnitCost * alloc);
  const y2UnitCost = spec.y1UnitCost * (1 + rate);
  const y3UnitCost = y2UnitCost * (1 + rate);
  return {
    y1Total,
    y2Units: spec.y1Units, y2UnitCost: years >= 3 ? y2UnitCost : 0,
    y2AllocPct: alloc, y2Total: years >= 3 ? Math.round(spec.y1Units * y2UnitCost * alloc) : 0,
    y3Units: spec.y1Units, y3UnitCost: years >= 3 ? y3UnitCost : 0,
    y3AllocPct: alloc, y3Total: years >= 3 ? Math.round(spec.y1Units * y3UnitCost * alloc) : 0,
  };
}

function lines(specs: LineInput[], years: number, startPos = 0): GeneratedLine[] {
  return specs.map((s, i) => ({
    ...s,
    isAutoGenerated: s.isAutoGenerated ?? true,
    y1AllocPct: s.y1AllocPct ?? 1,
    position: startPos + i,
    ...computeYears(s, years),
  }));
}

